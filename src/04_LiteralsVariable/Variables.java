/**
 * Class to show examples of variables.
 * 
 * @author Udayan Khattry
 */
public class Variables {
	public static void main(String [] args) {
		int x = 100; //declaration and initialization on the same line
		char var; //Only declaration, no initialization
		var = 'A'; //initializing var / assigning value to var 
		String name = "Udayan"; //Reference type declaration and initialization
		
		System.out.println("The value of x: " + x);
		System.out.println("The value of var: " + var);
		System.out.println("My name is " + name);
	}
}

	