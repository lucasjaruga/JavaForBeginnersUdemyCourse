/**
 * Class to show examples of String literals.
 * 
 * @author Udayan Khattry
 */
public class StringLietrals {
	public static void main(String [] args) {
		System.out.println("");
		System.out.println(" ");
		System.out.println("A");
		System.out.println("Java");
		System.out.println("Hello\n");
		System.out.println("\"");
	}
}
